import 'package:flutter/material.dart';

class CalculatorButtonTheme {
  final Color numberTextColor;
  final Color numberButtonColor;
  final Color numberShadow;
  final Color deleteButtonColor;
  final Color deleteShadow;
  final Color deleteTextColor;
  final Color resultButtonColor;
  final Color resultShadow;
  final Color resultTextColor;

  CalculatorButtonTheme({required this.numberTextColor, required this.numberButtonColor, required this.numberShadow, required this.deleteButtonColor, required this.deleteShadow, required this.deleteTextColor, required this.resultButtonColor, required this.resultShadow, required this.resultTextColor});
}